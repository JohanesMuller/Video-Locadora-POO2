    private void BtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtBuscarActionPerformed
        String valor = TextConsulta.getText();
        Gerente g = new Gerente();
        String tipoUsuario = "";
        lista = g.buscaAtendentes(valor);
        String[] col = {"Id", "Nome", "Usuario", "Senha", "TipoUsuario"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        for (Atendente a : lista) {
            if(a.getCodtipo() == 1){
                tipoUsuario = "Gerente";
            }else{
                tipoUsuario = "Atendente";
            }
            modelo.addRow(new String[]{String.valueOf(a.getId()), a.getNome(), a.getLogin(),
                a.getSenha(), tipoUsuario});
        }
        Tabela.setModel(modelo);
    }//GEN-LAST:event_BtBuscarActionPerformed

