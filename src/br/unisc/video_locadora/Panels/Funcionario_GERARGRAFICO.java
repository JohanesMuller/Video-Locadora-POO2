    private CategoryDataset createDataset() {
        List<Filmes> lista;
        Funcionario f = new Funcionario();
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        lista = f.buscaFilme("");
        graf = f.grafico(lista);
        for(GeradorDeGrafico gr : graf){
            dataset.addValue(gr.getQtd(), gr.getGenero(), gr.getGenero());
        }
        

        return dataset;
    }
    
    public void criaGrafico() {
        CategoryDataset cds = createDataset();
        String titulo = "CineMix";
        String eixoy = "Quantidade";
        String txt_legenda = "GÃªnero:";
        boolean legenda = true;
        boolean tooltips = true;
        boolean urls = true;
        JFreeChart graf = ChartFactory.createBarChart3D(titulo, txt_legenda, eixoy, cds, PlotOrientation.VERTICAL, legenda, tooltips, urls);
        ChartPanel myChartPanel = new ChartPanel(graf, true);
        myChartPanel.setSize(pnlGrafico.getWidth(), pnlGrafico.getHeight());
        myChartPanel.setVisible(true);
        pnlGrafico.removeAll();
        pnlGrafico.add(myChartPanel);
        pnlGrafico.revalidate();
        pnlGrafico.repaint();
    }

